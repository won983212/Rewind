package com.won983212.rewind.ui;

public class ComponentArea {
    public float x;
    public float y;
    public float width;
    public float height;


    public ComponentArea() {
        this(0, 0, 0, 0);
    }

    public ComponentArea(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
